var bigInt = require("./BigInteger.min.js");

function reverse(str) {
    return str
        .split("")
        .reverse()
        .join("");
}

function hex_str_to_bigint(hex_str) {
    result = bigInt("0");
    hex_str = reverse(hex_str.slice(2, hex_str.length));
    let idx = 0;
    for (i of hex_str) {
        result = result.add(
            bigInt("16")
                .pow(idx)
                .multiply(parseInt(i, 16)),
        );
        idx = idx + 1;
    }
    return result;
}

function get_different_base_indices(x, base) {
    var digits = [];
    while (x > 0) {
        digits.push(
            bigInt(x)
                .mod(base)
                .toJSNumber(),
        );
        x = bigInt(x).divide(base);
    }
    digits = digits.reverse();

    return digits;
}

function addr_to_mnemonic(addr) {
    if (addr.slice(0, 2) !== "0x") {
        throw new Error("Invalid input");
    }
    let number = hex_str_to_bigint(addr);
    let indices = get_different_base_indices(number, 2048);
    result = indices.map(i => words[i]).join(" ");
    return result;
}

function mnemonic_to_addr(mnemonic_str) {
    if (!mnemonic_str) {
        throw new Error("Invalid input");
    }
    let words_ = mnemonic_str.split(" ").filter(val => val);
    words_ = words_.reverse();
    let number = bigInt(0);
    let count = 0;
    for (word of words_) {
        number = number.add(
            bigInt(2048)
                .pow(count)
                .multiply(word_idx[word]),
        );
        count += 1;
    }
    let hex_indices = get_different_base_indices(number, 16);
    let result = "0x";
    for (idx of hex_indices) {
        result = result + hex_symbols[idx];
    }
    return result;
}

